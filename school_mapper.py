def _normalize_school_name(name: str) -> str:
    """规范化学校名称：去除空格/括号，尝试别名匹配"""
    name = name.strip().replace(" ", "")
    # 移除常见后缀噪声
    for suffix in ["（", "(", "分校", "校区"]:
        idx = name.find(suffix)
        if idx > 0:
            name = name[:idx]
    # 别名映射
    if name in _ALIAS_MAP:
        return _ALIAS_MAP[name]
    # 部分匹配别名
    for alias, full in _ALIAS_MAP.items():
        if alias in name:
            return full
    return name


def identify_school_tier(school_name: str) -> str:
    """
    识别学校层级

    Args:
        school_name: 用户输入的学校名称

    Returns:
        "985" / "211" / "普通本科" / "大专"
    """
    if not school_name or not school_name.strip():
        return "普通本科"

    # 先检查大专关键词（优先级高）
    for kw in _COLLEGE_KEYWORDS:
        if kw in school_name:
            return "大专"

    normalized = _normalize_school_name(school_name)

    # 精确匹配
    if normalized in SCHOOLS_985:
        return "985"
    if normalized in SCHOOLS_211_ONLY:
        return "211"

    # 模糊匹配：输入是否包含某985/211学校名称
    for s in SCHOOLS_985:
        if s in normalized or normalized in s:
            return "985"
    for s in SCHOOLS_211_ONLY:
        if s in normalized or normalized in s:
            return "211"

    return "普通本科"


