def get_ps_level_from_matrix(ps_base_level: int, kh_level: int) -> int:
    """
    安全获取PS Level（从PS矩阵查找）

    Args:
        ps_base_level: PS基础级别（1-19）
        kh_level: KH级别（16-57+）

    Returns:
        PS Level，超出范围时返回合理的边界值
    """
    # 边界处理：ps_base_level
    if ps_base_level < 1:
        ps_base_level = 1
    elif ps_base_level > 19:
        ps_base_level = 19

    # 边界处理：kh_level
    if kh_level < 16:
        kh_level = 16
    elif kh_level > 57:
        # 超出矩阵范围，使用线性外推
        # 规律：ps_level = kh_level - 16 + PS_MATRIX[ps_base_level][16]
        base_ps = PS_MATRIX[ps_base_level][16]
        return base_ps + (kh_level - 16)

    return PS_MATRIX[ps_base_level][kh_level]

